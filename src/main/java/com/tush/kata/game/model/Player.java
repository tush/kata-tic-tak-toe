package com.tush.kata.game.model;

/**
 * Player Model
 * The class helps hold data related to Player
 */
public class Player {
    private String name;
    private String id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
